void errormsg(const char* name, const char* desc, int err) {
  char msg[1024];
  int rc = strerror_r(err, msg, 1024);
  if (rc == 0) {
    fprintf(stderr, "%s %s: %s\n", name, desc, msg);
  } else {
    fprintf(stderr, "%s %s: errno:%d\n", name, desc, err);
  }
}

int main(int argc, char** argv) {
#if __MVS__
  __ae_autoconvert_state(_CVTSTATE_OFF);
#endif
  const unsigned char* table;
  struct stat st;
  int err;
  char msg[1024];
  if (argc < 3) {
    help(argc, argv);
  }
  if (0 == strcmp(argv[1], "-a2e")) {
    table = a2e;
  } else if (0 == strcmp(argv[1], "-e2a")) {
    table = e2a;
  } else {
    help(argc, argv);
  }
  char** files = argv + 2;
  int error = 0;
  while (*files) {
    int rc = stat(*files, &st);
    if (rc == -1) {
      errormsg(*files, "stat failed", errno);
      ++error;
    } else if (0 == (S_IWUSR & st.st_mode)) {
      fprintf(stderr, "%s is not writable, permssiion denied\n", *files);
      ++error;
    } else if (!S_ISREG(st.st_mode)) {
      fprintf(stderr, "%s is not a regular file, invalid\n", *files);
      ++error;
    } else if (st.st_size == 0) {
      printf("%s is 0 byte in size, skip\n", *files);
    } else {
      unsigned char buffer[4096];
      ssize_t bytes;
      int fd = open(*files, O_RDWR);
      if (-1 == fd) {
        errormsg(*files, "open failed", errno);
        ++error;
      } else {
        off_t off;
        do {
          off = lseek(fd, 0, SEEK_SET);
          if (off == -1) {
            errormsg(*files, "seek failed", errno);
            ++error;
            break;
          }
          bytes = read(fd, buffer, 4096);
          while (bytes > 0) {
            size_t i;
            for (i = 0; i < bytes; ++i) {
              buffer[i] = table[255 & buffer[i]];
            }
            off = lseek(fd, off, SEEK_SET);
            if (off == -1) {
              errormsg(*files, "seek failed", errno);
              ++error;
              break;
            }
            rc = write(fd, buffer, bytes);
            if (rc == -1) {
              errormsg(*files, "write failed", errno);
              ++error;
              break;
            }
            off = lseek(fd, 0, SEEK_CUR);
            if (off == -1) {
              errormsg(*files, "seek failed", errno);
              ++error;
              break;
            }
            bytes = read(fd, buffer, 4096);
          }
        } while (0);
        if (bytes < 0) {
          errormsg(*files, "read failed", errno);
          ++error;
          break;
        }
        close(fd);
      }
    }
    ++files;
  }
  return error;
}
